        /// <summary>
        /// 批量执行添加修改、修改或删除操作
        /// </summary>
        /// <param name="SQLString">查询语句</param>
        /// <returns>DataSet</returns>
        public static int AddUpdateOrDeleteList(IList<string> SQLStringArray, IList<SqlParameter[]> cmdParmsArray)
        {
            if (SQLStringArray != null && cmdParmsArray != null)
            {
                int totalCount = SQLStringArray.Count;
                if (totalCount > 0 && totalCount == cmdParmsArray.Count)
                {
                    //执行成功数据条数
                    int successCount = 0;
                    using (SqlConnection connection = new SqlConnection(SqlHelper.ConnectionString))
                    {
                        using (SqlCommand cmd = new SqlCommand())
                        {
                            cmd.Connection = connection;
                            cmd.CommandType = CommandType.Text;
                            try
                            {
                                connection.Open();

                                for (int i = 0; i < totalCount; i++)
                                {
                                    string sql = SQLStringArray[i];
                                    SqlParameter[] paras = cmdParmsArray[i];
                                    cmd.CommandText = sql;
                                    if (paras != null)
                                    {
                                        cmd.Parameters.AddRange(paras);
                                    }
                                    int result = cmd.ExecuteNonQuery();
                                    if (result > 0)
                                    {
                                        successCount++;
                                    }
                                    cmd.Parameters.Clear();
                                }
                            }
                            catch (Exception ex)
                            {
                                LogHelper.WriteMessage(string.Format("执行SQL语句时：\r\n{0}", ex));
                                successCount = -1;
                            }
                        }
                    }
                    return successCount;
                }
            }
            return 0;
        }

        private static void PrepareCommand(SqlCommand cmd, SqlConnection conn, SqlTransaction trans, string cmdText, SqlParameter[] cmdParms)
        {
            if (conn.State != ConnectionState.Open)
                conn.Open();
            cmd.Connection = conn;
            cmd.CommandText = cmdText;
            if (trans != null)
                cmd.Transaction = trans;
            cmd.CommandType = CommandType.Text;//cmdType;
            if (cmdParms != null)
            {


                foreach (SqlParameter parameter in cmdParms)
                {
                    if ((parameter.Direction == ParameterDirection.InputOutput || parameter.Direction == ParameterDirection.Input) &&
                        (parameter.Value == null))
                    {
                        parameter.Value = DBNull.Value;
                    }
                    cmd.Parameters.Add(parameter);
                }
            }
        }

